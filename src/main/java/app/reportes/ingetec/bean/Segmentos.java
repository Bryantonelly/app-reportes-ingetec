package app.reportes.ingetec.bean;

public class Segmentos {
	private int codigoSegmento;
	private String nombreSegmento;
	private Boolean estadoSegmento;
	
	public int getCodigoSegmento() {
		return codigoSegmento;
	}
	public void setCodigoSegmento(int codigoSegmento) {
		this.codigoSegmento = codigoSegmento;
	}
	public Boolean getEstadoSegmento() {
		return estadoSegmento;
	}
	public void setEstadoSegmento(Boolean estadoSegmento) {
		this.estadoSegmento = estadoSegmento;
	}
	public String getNombreSegmento() {
		return nombreSegmento;
	}
	public void setNombreSegmento(String nombreSegmento) {
		this.nombreSegmento = nombreSegmento;
	}
	
}
